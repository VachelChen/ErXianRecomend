package lzl.edu.com.movierecommend.entity;

import java.io.Serializable;
import java.util.Date;

public class MUser implements Serializable{
	private String userID;
	private String name;
	private String password;
	private String Email;
	private int sex;
	private int age;
	private String brithday;
	private String province;
	private String city;
	private Date registerDate;
	private int followingNum;
	private int fansNum;
	private int weipingNum;
	private String headImg;
	private boolean myFriend;


	public boolean isMyFriend() {
		return myFriend;
	}
	public void setMyFriend(boolean myFriend) {
		this.myFriend = myFriend;
	}
	
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getEmail() {
		return Email;
	}
	public void setEmail(String email) {
		Email = email;
	}
	public int getSex() {
		return sex;
	}
	public void setSex(int sex) {
		this.sex = sex;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getBrithday() {
		return brithday;
	}
	public void setBrithday(String brithday) {
		this.brithday = brithday;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public Date getRegisterDate() {
		return registerDate;
	}
	public void setRegisterDate(Date registerDate) {
		this.registerDate = registerDate;
	}
	public int getFollowingNum() {
		return followingNum;
	}
	public void setFollowingNum(int followingNum) {
		this.followingNum = followingNum;
	}
	public int getFansNum() {
		return fansNum;
	}
	public void setFansNum(int fansNum) {
		this.fansNum = fansNum;
	}
	public int getWeipingNum() {
		return weipingNum;
	}
	public void setWeipingNum(int weipingNum) {
		this.weipingNum = weipingNum;
	}
	public String getHeadImg() {
		return headImg;
	}
	public void setHeadImg(String headImg) {
		this.headImg = headImg;
	}

	@Override
	public String toString() {
		return "MUser{" +
				"userID='" + userID + '\'' +
				", name='" + name + '\'' +
				", password='" + password + '\'' +
				", Email='" + Email + '\'' +
				", sex=" + sex +
				", age=" + age +
				", brithday='" + brithday + '\'' +
				", province='" + province + '\'' +
				", city='" + city + '\'' +
				", registerDate=" + registerDate +
				", followingNum=" + followingNum +
				", fansNum=" + fansNum +
				", weipingNum=" + weipingNum +
				", headImg='" + headImg + '\'' +
				'}';
	}
}
